# Program: SciPy Constants and Special Functions
# Description:
# This program demonstrates some basic SciPy constants
# and special mathematical functions.
# ---------------------------------------------------------

from scipy import constants, special

# Display mathematical and physical constants
print("Value of pi:", constants.pi)
print("Speed of light:", constants.c)
print("Gravitational constant:", constants.G)

# Special mathematical functions
print("\nFactorial of 5:", special.factorial(5))
print("Square root of 16:", special.sqrt(16))
print("Exponential of 2:", special.exp10(2))
